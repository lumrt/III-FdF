#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*tmp;

	if (lst && f)
	{
		while (lst != NULL)
		{
			f(lst->content);
			tmp = lst -> next;
			lst = tmp;
		}
	}
}
